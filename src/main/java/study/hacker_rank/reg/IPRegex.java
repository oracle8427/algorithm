package study.hacker_rank.reg;

public class IPRegex {

	/*-
	 * IP Range : 000.000.000.000 ~ 255.255.255.255
	 * ([0-9]|[0-9][0-9]|[01]?[0-9][0-9]|2[0-4][1-9]|25[0-5])
	 * [0-9]												=>	[0-9]
	 * [0-9]|[0-9][0-9]										=>	[0-9] or [00-99]
	 * [0-9]|[0-9][0-9]|[01]?[0-9][0-9]						=>	[0-9] or [00-99] or [000-099] or [100-199]
	 * [0-9]|[0-9][0-9]|[01]?[0-9][0-9]|2[0-4][1-9]			=>	[0-9] or [00-99] or [000-099] or [100-199] or [200-249]
	 * [0-9]|[0-9][0-9]|[01]?[0-9][0-9]|2[0-4][1-9]			=>	[0-9] or [00-99] or [000-099] or [100-199] or [200-249]
	 * [0-9]|[0-9][0-9]|[01]?[0-9][0-9]|2[0-4][1-9]|25[0-5]	=>	[0-9] or [00-99] or [000-099] or [100-199] or [200-249] or [250-255]
	 */
	private static final String ipRange = "([0-9]|[0-9][0-9]|[01]?[0-9][0-9]|2[0-4][1-9]|25[0-5])";
	public static final String pattern = ipRange + "\\." + ipRange + "\\." + ipRange + "\\." + ipRange;

	public static void main(String[] args) {
		String[] testCase = { 
			"000.12.12.034",
			"121.234.12.12",
			"23.45.12.56",
			"00.12.123.123123.123",
			"122.23",
			"Hello.IP"
		};

		for (String ip : testCase)
			System.out.println(ip.trim().matches(pattern));

	}

}
